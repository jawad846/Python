msg1 = 'Python For Beginners'

# To calculate charactors - len()
# len() is a general purpose function
# used for online Application form

print(len(msg1))

# To make string to Upper case (This is not changing the string, it's creating new string)
print(msg1.upper())

# To make string to Lower case 
print(msg1.lower())

# To print the index value use find() [This case sensitive]
print(msg1.find('F'))

print(msg1.find('Beginners'))

# To Replace the charactors replace() [This case sensitive]
print(msg1.replace('For', 'Course For'))